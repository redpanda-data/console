import { AiOutlineDelete } from 'react-icons/ai';

import type { GeneralTabProps } from './types';
import { Badge } from '../../../../redpanda-ui/components/badge';
import { Button } from '../../../../redpanda-ui/components/button';
import { FormItem, FormLabel, FormMessage } from '../../../../redpanda-ui/components/form';
import { Input } from '../../../../redpanda-ui/components/input';
import { Textarea } from '../../../../redpanda-ui/components/textarea';

export const GeneralTab = ({
  knowledgeBase,
  isEditMode,
  formData,
  validationErrors,
  tagsArray,
  onUpdateFormData,
  onAddTag,
  onRemoveTag,
  onUpdateTag,
}: GeneralTabProps) => (
  <div className="flex flex-col gap-4">
    <h2 className="font-semibold text-lg">General</h2>

    <FormItem>
      <FormLabel>ID</FormLabel>
      <Input disabled value={knowledgeBase.id} />
    </FormItem>

    {isEditMode ? (
      <FormItem>
        <FormLabel>Display Name</FormLabel>
        <Input onChange={(e) => onUpdateFormData('displayName', e.target.value)} value={formData.displayName} />
        {validationErrors.displayName && <FormMessage>{validationErrors.displayName}</FormMessage>}
      </FormItem>
    ) : (
      <FormItem>
        <FormLabel>Display Name</FormLabel>
        <Input disabled value={knowledgeBase.displayName} />
      </FormItem>
    )}

    {isEditMode ? (
      <FormItem>
        <FormLabel>Description</FormLabel>
        <Textarea
          onChange={(e) => onUpdateFormData('description', e.target.value)}
          rows={3}
          value={formData.description}
        />
        {validationErrors.description && <FormMessage>{validationErrors.description}</FormMessage>}
      </FormItem>
    ) : (
      <FormItem>
        <FormLabel>Description</FormLabel>
        <div className="min-h-[78px] rounded-md border border-gray-200 bg-gray-50 p-3">
          <p className="text-gray-900 text-sm">
            {knowledgeBase.description || <span className="text-gray-400 italic">Not set</span>}
          </p>
        </div>
      </FormItem>
    )}

    <FormItem>
      <FormLabel>Retrieval API URL</FormLabel>
      <p className="mb-2 text-muted-foreground text-sm">
        This URL is automatically generated by the system for accessing the knowledge base.
      </p>
      <Input disabled value={knowledgeBase.retrievalApiUrl} />
    </FormItem>

    <div>
      <p className="mb-1 font-medium text-gray-700 text-sm">Tags</p>
      <p className="mb-2 text-muted-foreground text-sm">Labels can help you organize your knowledge bases.</p>
      {isEditMode ? (
        <div>
          {tagsArray.map((tag, index) => (
            <div className="mb-2 flex gap-2" key={index}>
              <Input
                className="flex-1"
                onChange={(e) => onUpdateTag(index, 'key', e.target.value)}
                placeholder="Key"
                value={tag.key}
              />
              <Input
                className="flex-1"
                onChange={(e) => onUpdateTag(index, 'value', e.target.value)}
                placeholder="Value"
                value={tag.value}
              />
              <Button onClick={() => onRemoveTag(index)} size="sm" variant="outline">
                <AiOutlineDelete />
              </Button>
            </div>
          ))}
          <Button className="mt-2" onClick={onAddTag} size="sm" variant="outline">
            <span className="mr-1">+</span>
            Add Tag
          </Button>
        </div>
      ) : (
        <div className="rounded-md border border-gray-200 bg-gray-50 p-3">
          {knowledgeBase.tags && Object.keys(knowledgeBase.tags).length > 0 ? (
            <div className="flex flex-wrap gap-2">
              {Object.entries(knowledgeBase.tags).map(([key, value]) => (
                <Badge key={key} variant="outline">
                  {key}: {value}
                </Badge>
              ))}
            </div>
          ) : (
            <p className="text-gray-400 text-sm italic">No tags configured</p>
          )}
        </div>
      )}
    </div>
  </div>
);
